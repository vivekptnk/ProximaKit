"""Aggregate a directory of BenchResult JSON files into Markdown tables.

Usage:
    python compare.py --in out/ --out compare.md
    python compare.py --in out/ --format markdown
    python compare.py --in out/ --format json   # machine-readable dump
"""

from __future__ import annotations

import argparse
import glob
import json
import os
import sys
from collections import defaultdict
from typing import Any


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Cross-library bench aggregator")
    p.add_argument("--in", dest="in_dir", required=True,
                   help="directory containing BenchResult JSON files")
    p.add_argument("--out", default=None,
                   help="output path (default: stdout)")
    p.add_argument("--format", choices=["markdown", "json"], default="markdown")
    return p.parse_args()


def main() -> int:
    args = parse_args()
    results = _load_all(args.in_dir)
    if not results:
        sys.stderr.write(f"[compare] no BenchResult files found under {args.in_dir}\n")
        return 1

    if args.format == "json":
        body = json.dumps(results, indent=2, sort_keys=True)
    else:
        body = _render_markdown(results)

    if args.out:
        with open(args.out, "w", encoding="utf-8") as fh:
            fh.write(body)
        sys.stderr.write(f"[compare] wrote {args.out}\n")
    else:
        sys.stdout.write(body)
    return 0


def _load_all(in_dir: str) -> list[dict[str, Any]]:
    out: list[dict[str, Any]] = []
    for path in sorted(glob.glob(os.path.join(in_dir, "*.json"))):
        try:
            with open(path, "r", encoding="utf-8") as fh:
                doc = json.load(fh)
        except Exception as exc:
            sys.stderr.write(f"[compare] skipping {path}: {exc}\n")
            continue
        if doc.get("skipped"):
            sys.stderr.write(f"[compare] skipping {path}: marked skipped\n")
            continue
        if doc.get("schemaVersion") != 1:
            sys.stderr.write(f"[compare] skipping {path}: unknown schemaVersion\n")
            continue
        doc["_path"] = os.path.basename(path)
        out.append(doc)
    return out


def _render_markdown(results: list[dict[str, Any]]) -> str:
    groups: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for r in results:
        groups[r["dataset"]].append(r)

    buf: list[str] = []
    buf.append("# Cross-Library Benchmark Comparison\n")
    buf.append(
        "Generated by `Benchmarks/python/compare.py` from `BenchResult` JSON "
        "documents (schema v1). Every row is a single harness run.\n"
    )

    for dataset in sorted(groups):
        rows = sorted(groups[dataset], key=lambda r: (r["library"], r["indexParams"].get("efSearch", 0)))
        first = rows[0]
        buf.append(f"\n## {dataset}\n")
        buf.append(
            f"- **Size**: {first['datasetSize']:,} vectors, {first['dimension']}d, metric `{first['metric']}`\n"
            f"- **k**: {first['k']}, queries: {first['queryCount']:,}\n"
            f"- **Runner**: {first['platform']['cpuModel']} / {first['platform']['os']} {first['platform']['kernel']}\n"
        )
        buf.append("\n")
        buf.append("| Library | Params | Build (s) | p50 (ms) | p95 (ms) | QPS | Recall@k | RSS (MB) |\n")
        buf.append("|---------|--------|-----------|----------|----------|-----|----------|----------|\n")
        for r in rows:
            params = _fmt_params(r["indexParams"])
            buf.append(
                "| {lib} | {params} | {build:.2f} | {p50:.2f} | {p95:.2f} | {qps:.0f} | {recall:.3f} | {rss:.1f} |\n".format(
                    lib=r["library"],
                    params=params,
                    build=r["buildTimeSeconds"],
                    p50=r["searchLatencyP50Ms"],
                    p95=r["searchLatencyP95Ms"],
                    qps=r["queriesPerSecond"],
                    recall=r["recallAt10"],
                    rss=r["residentMemoryMb"],
                )
            )

    buf.append(
        "\n---\n\n_Numbers are single-threaded wall-clock unless noted. "
        "Reproduce via `Benchmarks/README.md`._\n"
    )
    return "".join(buf)


def _fmt_params(params: dict[str, Any]) -> str:
    t = params.get("type", "?")
    if t == "hnsw":
        return f"HNSW M={params.get('M')} efC={params.get('efConstruction')} ef={params.get('efSearch')}"
    if t == "scann":
        return (
            f"ScaNN leaves={params.get('numLeaves')} "
            f"search={params.get('numLeavesToSearch')} reorder={params.get('reorder')}"
        )
    return ", ".join(f"{k}={v}" for k, v in params.items())


if __name__ == "__main__":
    sys.exit(main())
