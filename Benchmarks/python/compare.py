"""Aggregate a directory of BenchResult JSON files into Markdown tables.

Usage:
    python compare.py --in out/ --out compare.md
    python compare.py --in out/ --format markdown
    python compare.py --in out/ --format json   # machine-readable dump

Regression gate (used by the PR smoke job in .github/workflows/benchmark.yml):
    python compare.py --in out/ --min-recall 0.90 --gate-library ProximaKit

With --min-recall set, the process exits non-zero if any result row for the
gated library reports recallAt10 below the threshold, or if no rows for that
library are present at all (a silently missing run must not pass the gate).
"""

from __future__ import annotations

import argparse
import glob
import json
import os
import sys
from collections import defaultdict
from typing import Any


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Cross-library bench aggregator")
    p.add_argument("--in", dest="in_dir", required=True,
                   help="directory containing BenchResult JSON files")
    p.add_argument("--out", default=None,
                   help="output path (default: stdout)")
    p.add_argument("--format", choices=["markdown", "json"], default="markdown")
    p.add_argument("--min-recall", type=float, default=None,
                   help="exit non-zero if the gated library's recallAt10 falls "
                        "below this threshold on any result row")
    p.add_argument("--gate-library", default="ProximaKit",
                   help="library name the --min-recall gate applies to "
                        "(default: ProximaKit)")
    return p.parse_args()


def main() -> int:
    args = parse_args()
    results = _load_all(args.in_dir)
    if not results:
        sys.stderr.write(f"[compare] no BenchResult files found under {args.in_dir}\n")
        return 1

    if args.format == "json":
        body = json.dumps(results, indent=2, sort_keys=True)
    else:
        body = _render_markdown(results)

    if args.out:
        with open(args.out, "w", encoding="utf-8") as fh:
            fh.write(body)
        sys.stderr.write(f"[compare] wrote {args.out}\n")
    else:
        sys.stdout.write(body)

    if args.min_recall is not None:
        return _check_recall_gate(results, args.gate_library, args.min_recall)
    return 0


def _check_recall_gate(results: list[dict[str, Any]], library: str,
                       min_recall: float) -> int:
    """Return 0 if every row for `library` meets `min_recall`, else 1."""
    rows = [r for r in results if r.get("library") == library]
    if not rows:
        sys.stderr.write(
            f"[compare] GATE FAIL: no results for library '{library}' — "
            "a missing run must not pass the recall gate\n"
        )
        return 1
    failures = [r for r in rows if r.get("recallAt10", 0.0) < min_recall]
    for r in failures:
        sys.stderr.write(
            f"[compare] GATE FAIL: {r['_path']}: {library} recall@10 "
            f"{r.get('recallAt10', 0.0):.4f} < threshold {min_recall:.4f}\n"
        )
    if failures:
        return 1
    sys.stderr.write(
        f"[compare] gate passed: {len(rows)} {library} row(s) >= "
        f"recall {min_recall:.4f}\n"
    )
    return 0


def _load_all(in_dir: str) -> list[dict[str, Any]]:
    out: list[dict[str, Any]] = []
    for path in sorted(glob.glob(os.path.join(in_dir, "*.json"))):
        try:
            with open(path, "r", encoding="utf-8") as fh:
                doc = json.load(fh)
        except Exception as exc:
            sys.stderr.write(f"[compare] skipping {path}: {exc}\n")
            continue
        if doc.get("skipped"):
            sys.stderr.write(f"[compare] skipping {path}: marked skipped\n")
            continue
        if doc.get("schemaVersion") != 1:
            sys.stderr.write(f"[compare] skipping {path}: unknown schemaVersion\n")
            continue
        # GroundTruth__*.json shares schemaVersion=1 but is not a BenchResult
        # (no library/indexParams/recall fields). Skip anything that doesn't
        # carry a library identifier so the workflow's GT artifact in the
        # same out/ directory does not crash the aggregator.
        if "library" not in doc:
            sys.stderr.write(f"[compare] skipping {path}: not a BenchResult (no 'library' field)\n")
            continue
        doc["_path"] = os.path.basename(path)
        out.append(doc)
    return out


def _render_markdown(results: list[dict[str, Any]]) -> str:
    groups: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for r in results:
        groups[r["dataset"]].append(r)

    buf: list[str] = []
    buf.append("# Cross-Library Benchmark Comparison\n")
    buf.append(
        "Generated by `Benchmarks/python/compare.py` from `BenchResult` JSON "
        "documents (schema v1). Every row is a single harness run.\n"
    )

    for dataset in sorted(groups):
        rows = sorted(groups[dataset], key=lambda r: (r["library"], r["indexParams"].get("efSearch", 0)))
        first = rows[0]
        buf.append(f"\n## {dataset}\n")
        # Metric is rendered per-row below: a dataset group may legitimately
        # mix metrics (e.g. ScaNN evaluated as cosine against a cosine GT,
        # next to L2 HNSW rows), so a single group-level metric would lie.
        buf.append(
            f"- **Size**: {first['datasetSize']:,} vectors, {first['dimension']}d\n"
            f"- **k**: {first['k']}, queries: {first['queryCount']:,}\n"
            f"- **Runner**: {first['platform']['cpuModel']} / {first['platform']['os']} {first['platform']['kernel']}\n"
        )
        buf.append("\n")
        buf.append("| Library | Params | Metric | Build (s) | p50 (ms) | p95 (ms) | QPS | Recall@k | RSS (MB) |\n")
        buf.append("|---------|--------|--------|-----------|----------|----------|-----|----------|----------|\n")
        for r in rows:
            params = _fmt_params(r["indexParams"])
            buf.append(
                "| {lib} | {params} | {metric} | {build:.2f} | {p50:.2f} | {p95:.2f} | {qps:.0f} | {recall:.3f} | {rss:.1f} |\n".format(
                    lib=r["library"],
                    params=params,
                    metric=r["metric"],
                    build=r["buildTimeSeconds"],
                    p50=r["searchLatencyP50Ms"],
                    p95=r["searchLatencyP95Ms"],
                    qps=r["queriesPerSecond"],
                    recall=r["recallAt10"],
                    rss=r["residentMemoryMb"],
                )
            )

    buf.append(
        "\n---\n\n_Numbers are single-threaded wall-clock unless noted. "
        "Reproduce via `Benchmarks/README.md`._\n"
    )
    return "".join(buf)


def _fmt_params(params: dict[str, Any]) -> str:
    t = params.get("type", "?")
    if t == "hnsw":
        return f"HNSW M={params.get('M')} efC={params.get('efConstruction')} ef={params.get('efSearch')}"
    if t == "scann":
        return (
            f"ScaNN leaves={params.get('numLeaves')} "
            f"search={params.get('numLeavesToSearch')} reorder={params.get('reorder')}"
        )
    return ", ".join(f"{k}={v}" for k, v in params.items())


if __name__ == "__main__":
    sys.exit(main())
